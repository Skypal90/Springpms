package com.artek.webservices.model;

public class Employee {
	private int e_id;
	private String e_fname;
	private String e_lname;
	private Integer e_salary;

	public int getE_id() {
		return e_id;
	}

	public void setE_id(int e_id) {
		this.e_id = e_id;
	}

	public String getE_fname() {
		return e_fname;
	}

	public void setE_fname(String e_fname) {
		this.e_fname = e_fname;
	}

	public String getE_lname() {
		return e_lname;
	}

	public void setE_lname(String e_lname) {
		this.e_lname = e_lname;
	}

	public Integer getE_salary() {
		return e_salary;
	}

	public void setE_salary(Integer e_salary) {
		this.e_salary = e_salary;
	}

}
